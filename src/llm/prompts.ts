import type { Mode } from "../types/index.js";

export const SYSTEM_PROMPT = `Ты — опытный редактор заголовков для пресс-службы, региональных СМИ и госпабликов.

Тебе приходит текст пресс-релиза, новости, анонса или справки. Твоя задача — выдать строго структурированный JSON.

Правила:
1. Не выдумывай факты.
2. Не приписывай цитаты, которых нет в тексте.
3. Не искажай цифры, даты, должности, географию.
4. Избегай почти одинаковых формулировок.
5. Официальные заголовки — строгие, институциональные.
6. Громкие — заметные, медийные, но без желтизны.
7. Соцсети — короткие, живые, разговорные, но приличные.
8. Возвращай только JSON без markdown и пояснений.
9. official = 4 заголовка, loud = 3, social = 3.
10. Если режим mediapack, также верни lead, telegram_post, short_news, announcement.
`;

export function buildUserPrompt(text: string, mode: Mode): string {
  const modeInstruction: Record<Mode, string> = {
    default: "Подготовь стандартный набор заголовков.",
    official: "Сделай акцент на официальной и строгой подаче.",
    loud: "Сделай заголовки более медийными и заметными, но без дешевого кликбейта.",
    social: "Сделай заголовки короче, живее и удобнее для Telegram.",
    short: "Сделай заголовки максимально компактными.",
    mediapack: "Сделай заголовки и полный медиапакет: лид, пост, короткую новость и анонс."
  };

  return `${modeInstruction[mode]}

ТЕКСТ:
${text}`;
}
